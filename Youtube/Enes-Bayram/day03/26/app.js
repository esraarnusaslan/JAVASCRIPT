let number = prompt('please enter a number between 1 and 5');

switch (number) {
    case '1':
        console.log('the entered number is 1.');
        break;

    case '2':
        console.log('the entered number is 2.');
        break;

    case '3':
        console.log('the entered number is 3.');
        break;

    case '4':
        console.log('the entered number is 4.');
        break;

    case '5':
        console.log('the entered number is 5.');
        break;
    default:
        console.log('please enter a number between 1 and 5');
}
